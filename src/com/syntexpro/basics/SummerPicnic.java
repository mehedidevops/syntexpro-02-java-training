package com.syntexpro.basics;

public class SummerPicnic {

    String name;
    int cellPhone;
    int picnicChada;
    int age;

    public SummerPicnic(String name, int cellPhone, int picnicChada) {
        this.name = name;
        this.cellPhone = cellPhone;
        this.picnicChada = picnicChada;
    }

    public SummerPicnic(String name, int cellPhone, int picnicChada, int age) {
        this.name = name;
        this.cellPhone = cellPhone;
        this.picnicChada = picnicChada;
        this.age = age;

    }
}
